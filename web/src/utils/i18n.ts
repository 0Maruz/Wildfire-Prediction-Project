export interface LangCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, fallback?: string) => string;
}

export const LanguageContext = createContext<LangCtx>({
  lang: "en",
  setLang: () => undefined,
  t: (key, fallback) => fallback ?? key,
});

export function useLang(): LangCtx {
  return useContext(LanguageContext);
}

export function makeT(lang: Lang) {
  return (key: string, fallback?: string) => DICT[lang][key] ?? fallback ?? key;
}

// Replace {placeholder} tokens in a translated string. Missing values
// become empty strings (so partial substitutions don't print "{name}").
export function fmtTr(template: string, vars: Record<string, unknown>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = vars[k];
    return v == null ? "" : String(v);
  });
}
